def main():
    script_dir = os.path.dirname(os.path.abspath(__file__))
    project_dir = os.path.dirname(script_dir)
    resource_dir = os.path.join(project_dir, "resource")

    pongo_path = os.path.join(resource_dir, "Pongo.bin")
    output_path = os.path.join(resource_dir, "Pongo_lz4.bin")

    with open(pongo_path, "rb") as f:
        pongo = f.read()

    compressed = lz4.block.compress(pongo, mode="high_compression", compression=12, store_size=False)

    if len(compressed) + 512 > 0x80000:
        print(f"ERROR: compressed image too large ({len(compressed) + 512} > 524288)", file=sys.stderr)
        sys.exit(1)

    stub = bytearray(STUB)
    struct.pack_into("<I", stub, 508, len(compressed))

    image = bytes(stub) + compressed

    with open(output_path, "wb") as f:
        f.write(image)

    print(f"Pongo.bin:     {len(pongo)} bytes")
    print(f"LZ4 compressed: {len(compressed)} bytes")
    print(f"Output image:  {len(image)} bytes (stub + LZ4)")
    print(f"Written to:    {output_path}")


if __name__ == "__main__":
    main()
